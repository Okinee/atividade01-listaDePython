# Crie um progrma que peça a base e a altura de um triangulo, calcule sua area e mostre o resultado na tela.

base = int(input("Digite o valor da base"))
altura = int(input("Digite o valor da altura"))

area = base*altura

print("\nDe acordo com a area: {}, e a base {}, o valor da area foi de: {}".format(base, altura, area))
